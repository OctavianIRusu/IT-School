#apelare cu 1 parametru (capatul intervalului)

r1 = range(10)

for i in r1:
    print(i)

#apelare cu 2 parametri (capetele intervalului)

r2 = range(45, 91)

for j in r2:
    print(j)

#apelare cu 3 parametri (al treilea este pasul - negativ pentru pas descrescator)

r3 = range (40, 20, -2)

for k in r3:
    print(k)